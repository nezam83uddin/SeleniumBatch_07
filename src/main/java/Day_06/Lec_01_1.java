package Day_06;

public class Lec_01_1 {
	
	public static void main(String[] args) {
		for (int i = 0; i < 5; i++) {
			
			if (i==4) {
				System.out.println(i+" IF");
			} else if (i==1 || i==2 || i==3) {
				System.out.println(i+" 1st else IF");
			} 
			else {
				System.out.println(i+" Else");
			}
			System.out.println(i+" For Loop");
			
			
		}
		
	}

}
